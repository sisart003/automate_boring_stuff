# Custom Seating Cards
# Chapter 15 included a practice project to create custom invitations
# from a list of guests in a plaintext file. As an additional project, use the
# pillow module to create images for custom seating cards for your guests.
# For each of the guests listed in the guests.txt file from the resources at
# https://nostarch.com/automatestuff2/, generate an image file with the guest
# name and some flowery decoration. A public domain flower image is also
# available in the book's resources.
# To ensure that each seating card is the same size, add a black rectangle
# on the edges of the invitation image so that when the image is printed
# out, there will be a guideline for cutting. The PNG files that Pillow produces are set to 72 pixels per inch, so a 4×5-inch card would require a
# 288×360-pixel image.